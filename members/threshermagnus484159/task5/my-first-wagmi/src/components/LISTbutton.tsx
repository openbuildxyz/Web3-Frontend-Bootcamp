let ERC721ContractAddress = '0xE38ddEcBf920DC3D65d600CE4d2c7AF87C08F494'; 
let MARKETContractAddress = '0x0c692914B5207703dF8bd2F1eDb68Db7c91f3C4a';

const provider = new ethers.providers.Web3Provider(window.ethereum);
const signer = provider.getSigner();

function ListNFT() {
  const [input, setInput] = useState({
    contractAddress: '', 
    tokenId: '', 
    price: '', 
    tokenURL: '', 
  });

  const [isFormVisible, setIsFormVisible] = useState(false);

  const handleInputChange = (e: React.ChangeEvent<HTMLInputElement>) => {
    setInput({ ...input, [e.target.name]: e.target.value });
  };

  function getCurrentFormattedTimeWithoutMilliseconds() {
    const now = new Date(); 
    return now.toISOString() 
      .slice(0, 16) 
      .replace(/[-T:]/g, ''); 
  }

  const listNFT = async () => {
    try {

      const erc721contract = new ethers.Contract(ERC721ContractAddress, erc721ABI, signer);
      const marketcontract = new ethers.Contract(MARKETContractAddress, marketABI, signer);

      const approvalTx = await erc721contract.approve(MARKETContractAddress, input.tokenId);
      await approvalTx.wait();

      const listTx = await marketcontract.listNFT(input.contractAddress, input.tokenId, input.price,getCurrentFormattedTimeWithoutMilliseconds(),input.tokenURL);
      await listTx.wait();
      

      alert('NFT 上架成功');
      setIsFormVisible(false);
    } catch (error) {
      console.error('上架 NFT 时出错:', error);
      alert('上架 NFT 出错，请查看控制台日志。');
      setIsFormVisible(false);
    }
  };

  const toggleFormVisibility = () => setIsFormVisible(!isFormVisible);

  return (
    <div>
      {isFormVisible ? (
        <form onSubmit={(e) => { e.preventDefault(); listNFT(); }}>
          <div style={{
            position: 'fixed', top: 0, left: 0, right: 0, bottom: 0,
            backgroundColor: 'rgba(0, 0, 0, 0.5)', // 遮罩层背景
            zIndex: 1050, // 遮罩层的 z-index
            display: 'flex',
            justifyContent: 'center',
            alignItems: 'center',
           }}>
            <div >
            <label>合约地址: </label>
            <input
              type="text"
              name="contractAddress"
              placeholder="Enter contract address"
              value={input.contractAddress}
              onChange={handleInputChange}
            />
            </div>
            <div>
              <label>Token ID: </label>
              <input
                type="text"
                name="tokenId"
                placeholder="Enter token ID"
                value={input.tokenId}
                onChange={handleInputChange}
              />
            </div>
            <div>
            <label>价格 (ETH): </label>
            <input
              type="text"
              name="price"
              placeholder="Enter price in ETH"
              value={input.price}
              onChange={handleInputChange}
            />
            </div>
            <div>
              <label>Token URL: </label>
              <input
               type="text"
                name="tokenURL"
                placeholder="Enter token URI"
                value={input.tokenURL}
                onChange={handleInputChange}
              />
            </div>
            <button type="submit" >上架 NFT</button>
            <button type="button" onClick={toggleFormVisibility}>取消</button>
            </div>
            <div>
          </div>
        </form>
      ) : (
        <button onClick={toggleFormVisibility}
        style={{
          backgroundColor: 'white', // 按钮背景颜色
          color: 'black',            // 文字颜色
          border: 'none',            // 无边框
          padding: '10px 20px',      // 内边距
          borderRadius: '5px',       // 边框圆角
          cursor: 'pointer',         // 鼠标悬停时显示为指针
          margin: '0 10px',          // 外边距
          fontSize: '16px',          // 字体大小
      }}
        >上架 NFT</button>
      )}
    </div>
  );
}

export default ListNFT;